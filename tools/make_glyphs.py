def to_bytes(art: str, w: int, h: int) -> bytes:
    """Pack into 1bpp row-major MSB-first."""
    rows = [r.ljust(w)[:w] for r in art.strip("\n").split("\n")]
    if len(rows) != h:
        raise ValueError(f"expected {h} rows, got {len(rows)}: {rows}")
    out = bytearray()
    for row in rows:
        for byte_start in range(0, w, 8):
            b = 0
            for bit in range(8):
                col = byte_start + bit
                if col < w and row[col] == '#':
                    b |= 1 << (7 - bit)
            out.append(b)
    return bytes(out)

def emit(name: str, data: bytes, w: int, h: int) -> str:
    lines = [
        f"// {name}: {w}x{h}, {len(data)} bytes 1bpp",
        f"#define {name.upper()}_W {w}",
        f"#define {name.upper()}_H {h}",
        f"const uint8_t {name}[] PROGMEM = {{",
    ]
    hex_bytes = [f"0x{b:02X}" for b in data]
    for i in range(0, len(hex_bytes), 12):
        lines.append("    " + ", ".join(hex_bytes[i:i+12]) + ",")
    lines.append("};")
    lines.append("")
    return "\n".join(lines)

def main():
    out = [
        "// icons.h - generated by make_glyphs.py",
        "// 1-bit packed bitmaps, row-major, MSB-first within each byte.",
        "// Use drawIconMask(spr, x, y, color, mask, w, h) to blit.",
        "#pragma once",
        "#include <Arduino.h>",
        "",
    ]
    for name, art, w, h in ICONS:
        data = to_bytes(art, w, h)
        out.append(emit(name, data, w, h))
    with open(OUT, "w") as f:
        f.write("\n".join(out))
    print(f"Wrote {OUT}")

if __name__ == "__main__":
    main()
